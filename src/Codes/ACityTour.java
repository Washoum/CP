package Codes;

import FastIO.inputClass;
import java.io.PrintWriter;

public class ACityTour {
    public void solve(int testNumber, inputClass sc, PrintWriter out) {
        int t=sc.nextInt();
        while (t>0) {
            t--;
            int n = sc.nextInt();
            int m=sc.nextInt();
            int x,y;
            for (int i=0;i<m;i++){
                x=sc.nextInt();
                y=sc.nextInt();
            }
            out.println(n - 1);
        }
    }
}
